        public IList<CompanyLocationPoco> GetAll(params Expression<Func<CompanyLocationPoco, object>>[] navigationProperties)
        {
            using (SqlConnection conn = new SqlConnection(_connectionString))
            {
                SqlCommand cmd = new SqlCommand();
                cmd.Connection = conn;

                cmd.CommandText = @"SELECT [Id]
                                  ,[Company]
                                  ,[Country_Code]
                                  ,[State_Province_Code]
                                  ,[Street_Address]
                                  ,[City_Town]
                                  ,[Zip_Postal_Code]
                                  ,[Time_Stamp]
                              FROM [dbo].[Company_Locations]";

                conn.Open();
                SqlDataReader rdr = cmd.ExecuteReader();
                int counter = 0;
                CompanyLocationPoco[] pocos = new CompanyLocationPoco[10000];

                while (rdr.Read())
                {
                    CompanyLocationPoco poco = new CompanyLocationPoco();
                    poco.Id = (Guid)rdr["Id"];
                    poco.Company = (Guid)rdr["Company"];
                    poco.CountryCode = (String)rdr["Country_Code"];
                    poco.Province = rdr.IsDBNull(3)? (String)null :(String)rdr["State_Province_Code"];
                    poco.Street = rdr.IsDBNull(4) ? (String)null : (String)rdr["Street_Address"];
                    poco.City = rdr.IsDBNull(5) ? (String)null : (String)rdr["City_Town"];
                    poco.PostalCode = rdr.IsDBNull(6) ? (String)null : (String)rdr["Zip_Postal_Code"];
                    poco.TimeStamp = (Byte[])rdr["Time_Stamp"];

                    pocos[counter] = poco;
                    counter++;
                }
                conn.Close();

                return pocos.Where(p => p != null).ToList();
            }
        }

        public IList<CompanyLocationPoco> GetList(Expression<Func<CompanyLocationPoco, bool>> where, params Expression<Func<CompanyLocationPoco, object>>[] navigationProperties)
